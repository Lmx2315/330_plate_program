/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  HAL_IncTick();
  
  if (SysTickDelay != 0) {SysTickDelay--;}  
  if (TIMER1<0xfffffff0) TIMER1++;
  if (TIMER2<0xfffffff0) TIMER2++;
 
 
  timer_DMA2++;
  TIME_SYS++;
  TIME_TEST++;
  time_uart++;
}

